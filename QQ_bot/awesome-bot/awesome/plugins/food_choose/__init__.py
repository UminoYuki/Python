from jieba import posseg
from nonebot import CommandSession, IntentCommand, NLPSession, on_command, on_natural_language

from awesome.plugins.food_choose.data_source import get_food


@on_command('food', aliases=('吃', '吃啥', '今天吃点啥'))
async def weather(session: CommandSession):

    message = await get_food(1)
    print(message)
    await session.send(message)


# on_natural_language 装饰器将函数声明为一个自然语言处理器
# keywords 表示需要响应的关键词，类型为任意可迭代对象，元素类型为 str
# 如果不传入 keywords，则响应所有没有被当作命令处理的消息
@on_natural_language(keywords={'吃'})
async def _(session: NLPSession):
    # 去掉消息首尾的空白符
    stripped_msg = session.msg_text.strip()
    # 对消息进行分词和词性标注
    words = posseg.lcut(stripped_msg)

    city = None
    # 遍历 posseg.lcut 返回的列表
    for word in words:
        # 每个元素是一个 pair 对象，包含 word 和 flag 两个属性，分别表示词和词性
        if word.flag == 'ns':
            # ns 词性表示地名
            city = word.word
            break

    # 返回意图命令，前两个参数必填，分别表示置信度和意图命令名
    return IntentCommand(90.0, 'acgpicture', current_arg=city or '')